// Contoh penggunaan switch case
let hari = "Senin";
switch (hari) {
    case "Senin":
        document.write("<br>Hari ini adalah Senin");
        break;
    case "Selasa":
        document.write("<br>Hari ini adalah Selasa");
        break;
    default:
        document.write("<br>Hari ini adalah hari lain");
}
